-- Create Views
CREATE VIEW ORDER_VIEW AS
SELECT M.ORD_ID, C.C_ID, C.CNAME, C.ADDRESS, C.PHONE_NUMBER, C.EMAIL,
       D.PID, P.PNAME, D.QUANTITY, M.AMOUNT, M.DATE
FROM MYORDER M
JOIN ORDER_DETAILS D ON M.ORD_ID = D.ORD_ID
JOIN CUSTOMER C ON M.CID = C.C_ID
JOIN PRODUCT P ON P.PID = D.PID;
GO

CREATE VIEW CURRENTLY_AVAILABLE_STOCK AS
SELECT P.PID, P.PNAME, P.AMOUNT, S.QUANTITY
FROM PRODUCT P
JOIN STOCK S ON P.PID = S.PID;
GO

CREATE VIEW PRODUCT_VIEW AS
SELECT P.PID, P.PNAME, P.AMOUNT, P.VID, V.VNAME
FROM PRODUCT P
JOIN VENDOR V ON P.VID = V.VID;
GO

-- Create Stored Procedure
CREATE PROCEDURE customer_in
    @cid INT,
    @cname VARCHAR(50),
    @address VARCHAR(75),
    @phone_number BIGINT,
    @email VARCHAR(25)
AS
    INSERT INTO CUSTOMER VALUES(@cid, @cname, @address, @phone_number, @email);
GO

